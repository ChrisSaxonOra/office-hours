create table sensors ( 
  sensor_id integer generated by default as identity
    constraint sensor_pk primary key, 
  latitude  number(8, 6) not null
    constraint sens_lat_c check (
      latitude between -90 and 90
    ),
  longitude number(9, 6) not null
    constraint sens_long_c check (
      longitude between -180 and 180
    )
);



/* One table for all measurement types */
create table sensor_readings ( 
  sensor_id           references sensors
    /* on delete cascade /**/,
  reading_datetime    
    -- date not null,
    -- timestamp not null,
/*  temperature_in_c    number, 
  pressure_in_mbar    number,
  relative_humidity   number check ( relative_humidity between 0 and 100 ),
  wind_speed_m_per_s  number check ( wind_speed_m_per_s >= 0 ), 
  wind_direction      number check ( wind_direction between 0 and 360 ),
  rainfall_mm_per_min number check ( rainfall_mm_per_min >= 0 ), 
  constraint sere_heat_readings_c  -- all heat values or none
    check ( 

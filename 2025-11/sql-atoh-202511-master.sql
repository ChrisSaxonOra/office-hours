    ),/**/
  constraint sensor_reading 
    primary key ( sensor_id, reading_datetime )
);



/* Separate tables for each  */
create table sensor_heat_readings ( 
  sensor_id, reading_datetime,
  foreign key ( sensor_id, reading_datetime )
    references sensor_readings
    -- on delete cascade 
    deferrable initially immediate,
  temperature_in_c    number not null, 
  pressure_in_mbar    number not null,
  relative_humidity   number not null check ( relative_humidity between 0 and 100 ),
  constraint sensor_heat_reading 
    primary key ( sensor_id, reading_datetime )
);

create table sensor_wind_readings ( 
  sensor_id, reading_datetime,
  foreign key ( sensor_id, reading_datetime )
    references sensor_readings
    -- on delete cascade 
    deferrable initially immediate,
  wind_speed_m_per_s  number not null check ( wind_speed_m_per_s >= 0 ),  
  wind_direction      number not null check ( wind_direction between 0 and 360 ),
  constraint sensor_wind_reading 
    primary key ( sensor_id, reading_datetime )
);

create table sensor_rain_readings ( 
  sensor_id, reading_datetime,
  foreign key ( sensor_id, reading_datetime )
    references sensor_readings
    -- on delete cascade 
    deferrable initially immediate,
  rainfall_mm_per_min number not null check ( rainfall_mm_per_min >= 0 ), 
  constraint sensor_rain_reading 
    primary key ( sensor_id, reading_datetime )
);


/* Temp conversions */
create or replace function celcius_to_fahrenheit (
  degrees_celcius number
) return number deterministic as
begin
  dbms_session.sleep ( 0.02 ); -- simulate slow function
  return ( degrees_celcius / 5 * 9 ) + 32;
end;
